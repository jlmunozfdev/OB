/* If Else + if else */

let nota = 4;

if (nota === 5) {
    console.log('Has obtenido un sobresaliente');
} else if (nota === 4) {
    console.log('Buen trabajo, pero haberlo hecho mejor');
} else if (nota === 3) {
    console.log('Has obtenido un sufciente');
} else if (nota === 2) {
    console.log('No has aprobado por poco');
} else if (nota === 1) {
    console.log('No has aprobado, debes esforzarte más para la próximo');
} else {
    console.log('Error, introduce una nota entre el 1 y el 5');
}